package HashMaplab1;

public class Pessoa {
	
	private String pessoa;
    private int idade;
	
	public Pessoa(String pessoa, int idade) {
		this.pessoa = pessoa;
		this.idade = idade;
	}
	public String getPesso() {
		return pessoa;
	}
	public void setPesso(String pesso) {
		this.pessoa = pesso;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	

	}


